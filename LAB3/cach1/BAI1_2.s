	AREA RESET,DATA,READONLY
		DCD 0x20000000
		DCD Reset_Handler
	ALIGN
SUM  DCD 0
N 	 DCD 10
	AREA MYCODE, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
	
SUMUP PROC
	ADD R0,R0,R1
	SUBS R1,R1, #1
	BGT SUMUP
	BX LR 
ENDP
	
Reset_Handler
	LDR R1,N
	MOV R0, #0
	BL SUMUP
	LDR R3,=SUM
	STR R0, [R3]
	
STOP
	B STOP
	END